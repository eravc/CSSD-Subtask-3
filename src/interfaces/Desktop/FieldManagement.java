    public void fieldTableLoad(SetOfFields sf, JTable table, JScrollPane pane) {
        Vector tmp = new Vector<>();

        for (Field item : sf) {
            Vector row = new Vector();
            row.addElement(item.getFieldID());
            row.addElement(item.getLocation());
            row.addElement(item.getLontitude());
            row.addElement(item.getLatitude());
            row.addElement(item.getOwner().getName());
            row.addElement(item.getCrop().getName());

            tmp.add(row);
        }

        //set column headings
        Vector columnNames = new Vector();
        columnNames.addElement("Field ID");
        columnNames.addElement("Location");
        columnNames.addElement("Longitude");
        columnNames.addElement("Lattitude");
        columnNames.addElement("Owner");
        columnNames.addElement("Crop");
        //set table model
        DefaultTableModel model = new DefaultTableModel(tmp, columnNames);

        table.setModel(model);
        pane.setViewportView(table);
        table.setAutoscrolls(true);
        table.getTableHeader().setReorderingAllowed(false);
    }

