    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = Integer.parseInt(jTextField1.getText());
        double longe = Double.parseDouble(jTextField9.getText().toString());
        double lati = Double.parseDouble(jTextField2.getText().toString());
        String location = jTextField3.getText().toString();
        int ownerid = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int cropid = Integer.parseInt(jComboBox2.getSelectedItem().toString());

        if (jTextField1.getText().isEmpty() | jTextField2.getText().isEmpty() | jTextField3.getText().isEmpty() | jTextField9.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill up the fields.");
        } else {
            Farmer owner = sou.getFarmerById(ownerid);
            Crop cp = soc.findCropById(cropid);
            Field fd = new Field(id, lati, longe, location, owner, cp);
            sof.addField(fd);

            try {
                sof.serialize();
                JOptionPane.showMessageDialog(rootPane, "Field successfully Added");

                sof.deSerialize();
                fieldTableLoad(sof, jTable1, jScrollPane1);
                fieldTableLoad(sof, jTable2, jScrollPane2);
                fieldTableLoad(sof, jTable3, jScrollPane3);

                jTextField1.setText(null);
                jTextField2.setText(null);
                jTextField3.setText(null);
                jTextField9.setText(null);
            } catch (Exception e1) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

