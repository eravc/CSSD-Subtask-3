    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTextField7.getText().isEmpty() | jTextField8.getText().isEmpty() | jTextField10.getText().isEmpty() | jTextField11.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill up the fields.");
        } else {
            int fieldid = Integer.parseInt(jTextField7.getText());
            double longe = Double.parseDouble(jTextField8.getText().toString());
            double lati = Double.parseDouble(jTextField10.getText().toString());
            String location = jTextField11.getText().toString();
            String oname = jLabel18.getText().toString();
            String cname = jLabel19.getText().toString();
            Farmer owner = sou.getFarmerByName(oname);
            Crop cp = soc.findCropByName(cname);
            
            sof.removeField(sof.findFieldById(fieldid));

            Field fd = new Field(fieldid, longe, lati, location, owner, cp);
            sof.addField(fd);

            try {

                sof.serialize();
                JOptionPane.showMessageDialog(rootPane, "Field successfully Eddited");

                soc.deserialize();
                fieldTableLoad(sof, jTable1, jScrollPane1);
                fieldTableLoad(sof, jTable2, jScrollPane2);
                fieldTableLoad(sof, jTable3, jScrollPane3);

                jTextField7.setText(null);
                jTextField8.setText(null);
                jTextField10.setText(null);
                jTextField11.setText(null);
            } catch (Exception e1) {
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

