    public void corpTableLoad(SetOfCrops co, JTable table, JScrollPane pane) {
        Vector tmp = new Vector<>();

        for (Crop item : co) {
            Vector row = new Vector();
            row.addElement(item.getCropID());
            row.addElement(item.getName());
            row.addElement(item.getApproxSpace());

            tmp.add(row);
        }

        //set column headings
        Vector columnNames = new Vector();
        columnNames.addElement("Corp ID");
        columnNames.addElement("Name");
        columnNames.addElement("Space");
        //set table model
        DefaultTableModel model = new DefaultTableModel(tmp, columnNames);

        table.setModel(model);
        pane.setViewportView(table);
        table.setAutoscrolls(true);
        table.getTableHeader().setReorderingAllowed(false);
    }

