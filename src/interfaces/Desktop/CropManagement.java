    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = Integer.parseInt(jTextField1.getText());
        String name = jTextField2.getText().toString();
        double space = Double.parseDouble(jTextField3.getText().toString());

        if (jTextField1.getText().isEmpty() | jTextField2.getText().isEmpty() | jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill up the fields.");
        } else {
            Crop cp = new Crop(id, name, space);
            soc.addCrop(cp);

            try {

                soc.serialize();
                JOptionPane.showMessageDialog(rootPane, "Corp successfully Added");

                soc.deserialize();
                corpTableLoad(soc, jTable1, jScrollPane1);
                corpTableLoad(soc, jTable2, jScrollPane2);
                corpTableLoad(soc, jTable3, jScrollPane3);

                jTextField1.setText(null);
                jTextField2.setText(null);
                jTextField3.setText(null);
            } catch (Exception e1) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

